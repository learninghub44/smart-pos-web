// ── Context types ───────────────────────────────────────────────
interface I18nContextValue {
  language: string
  currency: string
  setLanguage: (lang: string) => void
  setCurrency: (cur: string) => void
  t: (key: string) => string
  fmt: (amount: number) => string
  currencySymbol: string
}

const I18nContext = createContext<I18nContextValue | null>(null)

// ── Provider ────────────────────────────────────────────────────
export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLangState] = useState('en')
  const [currency, setCurrState] = useState('KES')

  useEffect(() => {
    try {
      const lang = localStorage.getItem('pos_language') || 'en'
      const cur  = localStorage.getItem('pos_currency') || 'KES'
      setLangState(lang)
      setCurrState(cur)
    } catch {}
  }, [])

  const setLanguage = useCallback((lang: string) => {
    setLangState(lang)
    try { localStorage.setItem('pos_language', lang) } catch {}
  }, [])

  const setCurrency = useCallback((cur: string) => {
    setCurrState(cur)
    try { localStorage.setItem('pos_currency', cur) } catch {}
  }, [])

  const t = useCallback((key: string): string => {
    return translations[language]?.[key] ?? translations['en']?.[key] ?? key
  }, [language])

  const fmt = useCallback((amount: number): string => {
    const cur = CURRENCIES.find(c => c.code === currency) || CURRENCIES[0]
    const rounded = Math.round(amount || 0)
    return `${cur.symbol} ${rounded.toLocaleString()}`
  }, [currency])

  const currencySymbol = CURRENCIES.find(c => c.code === currency)?.symbol ?? currency

  return (
    <I18nContext.Provider value={{ language, currency, setLanguage, setCurrency, t, fmt, currencySymbol }}>
      {children}
    </I18nContext.Provider>
  )
}

// ── Hook ────────────────────────────────────────────────────────
export function useI18n() {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useI18n must be used inside I18nProvider')
  return ctx
}
